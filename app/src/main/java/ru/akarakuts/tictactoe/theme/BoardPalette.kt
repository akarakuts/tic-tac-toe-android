/** Unlockable board colour themes over Material baseline + accent tint (parity with macOS palettes). */
data class BoardPalette(
    val sceneBackground: Color,
    val panelFill: Color,
    val panelStroke: Color,
    val boardShadow: Color,
    val boardPlaqueFill: Color,
    val boardPlaqueStroke: Color,
    val cellEven: Color,
    val cellOdd: Color,
    val gridLine: Color,
    val gridOuter: Color,
    val crossMark: Color,
    val noughtMark: Color,
    val statusText: Color,
    val captionText: Color,
    val pillFill: Color,
    val pillFillSelected: Color,
    val pillStroke: Color,
    val pillStrokeSelected: Color,
    val pillText: Color,
    val pillTextDisabled: Color,
    val newGameFill: Color,
    val newGameStroke: Color,
    val newGameLabel: Color,
    val winLine: Color,
)

fun boardPalette(style: BoardVisualStyle, darkMode: Boolean, accent: Color): BoardPalette =
    when (style) {
        BoardVisualStyle.Classic -> if (darkMode) classicDark(accent) else classicLight(accent)
        BoardVisualStyle.Aurora -> if (darkMode) auroraDark(accent) else auroraLight(accent)
        BoardVisualStyle.Grove -> if (darkMode) groveDark(accent) else groveLight(accent)
        BoardVisualStyle.Ember -> if (darkMode) emberDark(accent) else emberLight(accent)
    }

private fun gray(w: Float, a: Float = 1f) = Color(w, w, w, a)

private fun rgb(r: Float, g: Float, b: Float, a: Float = 1f) = Color(r, g, b, a)

private fun hueCalibrated(h: Float, s: Float, b: Float, a: Float = 1f): Color {
    val hsv = floatArrayOf(h * 360f, s.coerceIn(0f, 1f), b.coerceIn(0f, 1f))
    val c = android.graphics.Color.HSVToColor(hsv)
    return Color(
        android.graphics.Color.red(c) / 255f,
        android.graphics.Color.green(c) / 255f,
        android.graphics.Color.blue(c) / 255f,
        a,
    )
}

